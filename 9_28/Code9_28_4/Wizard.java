
public class Wizard {
	private String name;
	private int hp;
	
	public Wizard(String name, int hp) {
		this.setName(name);
		this.setHp(hp);
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getHp() {
		return hp;
	}

	public void setHp(int hp) {
		this.hp = hp;
	}
	

}
